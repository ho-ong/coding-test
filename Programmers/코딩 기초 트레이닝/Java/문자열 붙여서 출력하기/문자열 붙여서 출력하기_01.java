import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.next();
        String b = sc.next();

        // concat(): 문자열 병합
        // 문자열 a와 문자열 b를 병합
        System.out.println(a.concat(b));
    }
}
