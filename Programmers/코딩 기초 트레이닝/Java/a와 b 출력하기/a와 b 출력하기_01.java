import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        // print() 함수 사용 시 \n을 통해 줄 바꿈 가능
        System.out.print("a = " + a + "\nb = " + b);
    }
}
